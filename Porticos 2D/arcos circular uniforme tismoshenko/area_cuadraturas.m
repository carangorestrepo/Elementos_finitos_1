function area=area_cuadraturas(a,b,f)
datos=10;
area=0;
[xi,wi,~] = gausslegendre_quad(datos);
%xi=[-0.973906528517168;-0.865063366688988;-0.679409568299025;-0.433395394129247;-0.148874338981631;0.148874338981631;0.433395394129247;0.679409568299024;0.865063366688990;0.973906528517167];
%wi=[0.0666713443086981;0.149451349150575;0.219086362515982;0.269266719309996;0.295524224714753;0.295524224714753;0.269266719309997;0.219086362515982;0.149451349150574;0.0666713443087017];
for i=1:datos
    area=area+(b-a)/2*wi(i)*f((b-a)/2*xi(i)+(a+b)/2);
end